    def classify(self, email: Email) -> str:
        """Классифицируем письмо и возвращаем категорию"""
        scores = self._count_trigger_words(email)
        max_score = max(scores.values())
        winners = [category for category, score in scores.items() if score == max_score]
        if max_score == 0:
            logging.info(f'Письмо "{email.path.name}" отнесено в "Неизвестное"')
            return "Неизвестное", email.path
        elif len(winners) == 1:
            logging.info(f'Письмо "{email.path.name}" отнесено в "{winners[0]}"')
            return winners[0], email.path
        else:
            logging.info(f'Письмо "{email.path.name }" отнесено в "Смешанное"')
            return "Смешанная категория", email.path

    def _count_trigger_words(self, email: Email) -> dict:
        """Считаем количество триггерных слов в письме и возвращаем их"""
        scores = {
        "Инциденты": 0,
        "Техподдержка": 0,
        "Оборудование": 0,
        "Доступы": 0,
        "Финансы и документы": 0,
        "График работы": 0,
        "Спам": 0,
        "Встречи": 0,
        "Новостная сводка": 0,
        "Мониторинг": 0,
        }
        subject = email.subject.lower()
        body = email.body.lower()
        for word, category in self.trigger_words.items():
            if word in subject:
                scores[category] += 2
            if word in body:
                scores[category] += 1
        return scores